public class Rainfall
{

    public static void main (String args[])
    {
        final double AprilRainfall = 12.0;
        final double MayRainfall = 14.0;
        final double JuneRainfall = 8.0;
    System.out.println("Rainfall for April: " + AprilRainfall);
    System.out.println("Rainfall for May: " + MayRainfall);
    System.out.println("Rainfall for June: " + JuneRainfall);
    System.out.println("Average rainfall: " + (AprilRainfall + MayRainfall + JuneRainfall)/3);
    }
}